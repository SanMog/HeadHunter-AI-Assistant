import google.generativeai as genai
import json
import os
import time

# --- КОНФИГУРАЦИЯ ---
# Сколько топ-вакансий отправляем на анализ в Gemini
START_FROM_VACANCY = 20  # ### ИЗМЕНЕНИЕ: С какой по счету вакансии начинаем (считая с 0). Ставим 10, чтобы начать с 11-й.
PROCESS_N_VACANCIES = 10 # ### ИЗМЕНЕНИЕ: Сколько вакансий обработать.
INPUT_FILE = 'ranked_vacancies.json'
OUTPUT_FILE = 'cover_letters.md'

# --- ШАБЛОН ПРОМПТА НА ОСНОВЕ ВАШЕЙ СТРАТЕГИИ ---
PROMPT_TEMPLATE = """
# Задача: Проанализировать текст вакансии для QA-инженера и определить, подходит ли она кандидату.

Ты — опытный IT-рекрутер и карьерный консультант. Твоя задача — помочь кандидату по имени Александр Могилин, проанализировав текст вакансии. Ты должен вынести четкий вердикт ("Подходит" или "Не подходит") и, если вакансия подходит, составить для нее короткое, сильное сопроводительное письмо в утвержденном стиле.

---

### **Профиль Кандидата: Александр Могилин**

*   **Целевая роль:** QA Engineer (Middle / Middle+).
*   **Ключевое УТП:** QA-инженер с продуктовым мышлением. Человек, который не просто ищет баги, а понимает бизнес-цели и помогает улучшать продукт.
*   **Сильные стороны:**
    -   Гибридный опыт (QA + Project Management).
    -   Практический опыт построения QA-процессов и автоматизации с нуля (Python + Selenium).
    -   Опыт в сложных доменах (FinTech, GovTech).
    -   Проактивность и системное мышление.
*   **Зоны роста:** Неуверенность в "ручном" написании сложного кода для автотестов "с нуля" под давлением. Следует избегать чисто Senior AQA позиций, где требуется проектирование сложных фреймворков.
*   **Технический стек:**
    -   **Автоматизация:** Python, Selenium, Pytest, Winium, Postman (API-тесты).
    -   **Инструменты:** Jira, TestIT, Confluence, Git, Jenkins, Docker, Kibana.
    -   **Базы данных:** SQL (Oracle, PostgreSQL, MySQL).
*   **Зарплатные ожидания:** Цель — 140 000 ₽ на руки. Рассматриваем вакансии от 80 000 ₽, если они идеально подходят для роста.
*   **Формат работы:** Удаленный или гибридный в Санкт-Петербурге.

---

### **Правила Анализа Вакансии**

**1. Сначала проверь на "Красные флаги". Если находишь хотя бы один, вердикт — "Не подходит".**
    -   **Роль:** Явное требование опыта управления командой ("Lead", "Head of", "Руководитель").
    -   **Стек:** Жесткое требование опыта автоматизации на Java, C#, TypeScript (от 1-2 лет).
    -   **Домен:** Узкая специализация, где у кандидата нет опыта (1С, Gamedev, Hardware).
    -   **Зарплата:** Явно ниже 80 000 ₽.

**2. Если "Красных флагов" нет, ищи "Зеленые флаги", чтобы вынести вердикт "Подходит".**
    -   **"Золотые" фразы:** "строить процессы с нуля", "единственный QA", "оптимизация QA", "проактивный подход".
    -   **Стек:** Упоминание Python, Selenium, Pytest.
    -   **Задачи:** Гибридная роль (ручное + автоматизация), Junior Automation, фокус на API/backend, тестирование desktop-приложений.
    -   **Домен:** FinTech, GovTech.

---

### **Формат Вывода**

**Если вакансия не подходит:**
[Название компании] / [Название вакансии]

Анализ:
Нет, не подходит. [Кратко указать основную причину, ссылаясь на "Красные флаги"].

Так как вакансия не подходит, сопроводительное письмо не готовим.

**Если вакансия подходит:**
[Название компании] / [Название вакансии]

Анализ:
Да, подходит. [Кратко указать, почему, ссылаясь на "Зеленые флаги" и соответствие профилю кандидата].

Сопроводительное письмо

Тема: Отклик на вакансию [Название вакансии]

Добрый день.

Меня зовут Александр.

[Основной абзац 1, бьющий в ключевое требование вакансии и подкрепленный фактом из резюме кандидата].

[Основной абзац 2, закрывающий второе по важности требование или подчеркивающий уникальное преимущество кандидата (например, опыт с desktop)].

[Основной абзац 3, если нужен, для демонстрации дополнительных релевантных навыков].

Буду рад обсудить детали.

С уважением,
Александр Могилин
Telegram: @SanMog

---

### **Текст Вакансии для Анализа:**

{vacancy_text}
"""


def main():
    print("--- Шаг 4: Генерация сопроводительных писем ---")

    # Конфигурация Gemini API
    try:
        api_key = os.environ.get("GOOGLE_API_KEY")
        if not api_key:
            print("Ошибка: Переменная окружения GOOGLE_API_KEY не найдена.")
            print(
                "Инструкция: выполните в терминале `set GOOGLE_API_KEY=ВАШ_КЛЮЧ` (для Windows) или `export GOOGLE_API_KEY='ВАШ_КЛЮЧ'` (для Linux/Mac).")
            return
        genai.configure(api_key=api_key)
        model = genai.GenerativeModel('models/gemini-pro-latest')
        print("API Gemini успешно настроен.")
    except Exception as e:
        print(f"Произошла ошибка при настройке API: {e}")
        return

    # Загрузка ранжированных вакансий
    if not os.path.exists(INPUT_FILE):
        print(f"Ошибка: Файл {INPUT_FILE} не найден. Запустите Шаг 3.")
        return

    with open(INPUT_FILE, 'r', encoding='utf-8') as f:
        ranked_vacancies = json.load(f)

    start = START_FROM_VACANCY
    end = START_FROM_VACANCY + PROCESS_N_VACANCIES
    top_vacancies = ranked_vacancies[start:end]
    print(f"Загружено {len(top_vacancies)} вакансий для генерации писем.")

    # Очищаем/создаем выходной файл
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        f.write(f"# Автоматически сгенерированные сопроводительные письма\n\n")

    # Обработка каждой вакансии
    for i, vacancy in enumerate(top_vacancies):
        print(f"\n[{i + 1}/{len(top_vacancies)}] Обрабатываю вакансию: {vacancy['name']}...")

        vacancy_full_text = f"Название: {vacancy['name']}\nКомпания: {vacancy['company_name']}\n\nОписание:\n{vacancy['description']}"

        full_prompt = PROMPT_TEMPLATE.format(vacancy_text=vacancy_full_text)

        try:
            response = model.generate_content(full_prompt)
            generated_text = response.text
            print("Ответ от Gemini получен.")

            # Сохраняем результат
            with open(OUTPUT_FILE, 'a', encoding='utf-8') as f:
                f.write(f"## {i + 1}. {vacancy['name']} в {vacancy['company_name']}\n\n")
                f.write(f"**Ссылка:** {vacancy['url']}\n\n")
                f.write(f"**Оценка нашего скрипта:** {vacancy['score']}\n\n")
                f.write("---\n\n")
                f.write(generated_text)
                f.write("\n\n---\n\n")

        except Exception as e:
            print(f"!!! Произошла ошибка при обращении к API Gemini: {e}")
            with open(OUTPUT_FILE, 'a', encoding='utf-8') as f:
                f.write(f"## {i + 1}. {vacancy['name']} - ОШИБКА\n\n")
                f.write(f"Не удалось сгенерировать письмо из-за ошибки: {e}\n\n---\n\n")

        # Задержка между запросами, чтобы не превышать лимиты
        time.sleep(1)

    print(f"\nГотово! Все сопроводительные письма сохранены в файл: {OUTPUT_FILE}")


if __name__ == "__main__":
    main()